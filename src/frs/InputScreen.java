    /**
     * To check if a date is valid or not
     * @return true the selected date is valid
     */
    private boolean isDateValid() {
        //JOptionPane.showMessageDialog(this, jDateChooser1.getDate());
        DateTime date=null;
        try {
            date  = new DateTime(jDateChooser1.getDate());
        } catch (Exception e) {
            return false;
        }
        DateTimeFormatter dateTimeFormatter=DateTimeFormat.forPattern("dd/MM/yyyy");
        DateTime start = dateTimeFormatter.parseDateTime("30/09/2016");
        DateTime end   = dateTimeFormatter.parseDateTime("14/11/2016");
        
        if (date.isAfter(start) && date.isBefore(end)) {
                return true;
        }       
        return false;
    }
    
