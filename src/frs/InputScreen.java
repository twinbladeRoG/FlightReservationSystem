    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        if (isDateValid()) {
                    displayManager.setData(
                    jComboBox1.getSelectedItem().toString().toUpperCase(),
                    jComboBox2.getSelectedItem().toString().toUpperCase(),
                    Integer.parseInt(jSpinner1.getValue().toString()),
                    new DateTime(jDateChooser1.getDate())
            );;
            displayManager.callSearch();
            displayManager.showSearchResultScreen();           
        }
        else 
            JOptionPane.showMessageDialog(null,"Bookings are available from:\n1st Oct, 16 till 13th Nov, 16",
                    "Warning!", JOptionPane.WARNING_MESSAGE); 
    }//GEN-LAST:event_jLabel11MouseClicked

